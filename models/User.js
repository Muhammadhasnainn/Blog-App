import mongoose from "mongoose";

const UserSchema = mongoose.Schema({
  img: {
    data: Buffer,
    type: String,
  },
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

export default mongoose.model("user", UserSchema);
